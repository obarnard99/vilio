@DATASETS.register_module()
class GQA(CustomDataset):

    CLASSES = tuple(GQA_CLASSES)
    ATTR_CLASS = tuple(GQA_ATTRS)
    MAX_ATTR_PER_BOX = 5

    def pad_attr(self, boxes_attr):
        padded = []
        pad_const = len(GQA_ATTRS)
        for attr in boxes_attr:
            attr = list(set(attr))
            p = attr[:self.MAX_ATTR_PER_BOX] + [pad_const] * \
                max(0, self.MAX_ATTR_PER_BOX - len(attr))
            padded.append(p)
        return padded

    def load_annotations(self, ann_file):
        # ann_list = mmcv.list_from_file(ann_file)
        with open(ann_file, mode='r') as f:
            ann_list = json.load(f)

        data_infos = []
        for i, ann_line in enumerate(ann_list):
            if len(ann_line['boxes']) == 0:
                continue

            width = ann_line['width']
            height = ann_line['height']

            _bboxes = np.array(ann_line['boxes'])
            bboxes = np.zeros_like(_bboxes)
            bboxes[:, 0::2] = np.clip(_bboxes[:, 0::2], 1, width - 1)
            bboxes[:, 1::2] = np.clip(_bboxes[:, 1::2], 1, height - 1)
            labels = ann_line['class_id']

            if ((bboxes[:, 2] - bboxes[:, 0]) < 1).any():
                import pdb; pdb.set_trace()
            if ((bboxes[:, 3] - bboxes[:, 1]) < 1).any():
                import pdb; pdb.set_trace()
            if np.isnan(bboxes).any() or np.isnan(bboxes[:, 2:] - bboxes[:, :2]).any():
                import pdb; pdb.set_trace()


            # attrs = [[3] * random.randint(1, 3)] * len(labels)
            attrs = self.pad_attr(ann_line['attribute_idx'])
            try:
                labels = np.array(labels).astype(np.int64)
                attrs = np.array(attrs).astype(np.int64)
                bboxes = bboxes.astype(np.float32)

                if (attrs > len(GQA_ATTRS)).any():
                    raise RuntimeError('1')
                for attr in attrs:
                    counting = [0 for _ in range(len(GQA_ATTRS) + 1)]
                    for i in attr:
                        if i != len(GQA_ATTRS):
                            i = int(i)
                            counting[i] += 1
                            if counting[i] > 1:
                                raise RuntimeError('2 ' + str(attr))
            except Exception as e:
                print("LOlllll: ", e)
                import pdb; pdb.set_trace()
            data_infos.append(
                dict(
                    filename=ann_line['image_name'],
                    width=width,
                    height=height,
                    ann=dict(
                        bboxes=bboxes,
                        labels=labels,
                        attrs=attrs)
                ))

        return data_infos

    def get_ann_info(self, idx):
        return self.data_infos[idx]['ann']
